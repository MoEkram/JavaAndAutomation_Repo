package l_Constructor;

// Constructor  - Block of code in each Class
//              - Has the same class name
//              - Initialize when create object from the class
//                   ex: MyClass obj = new MyClass (); << this is constructor
//              - Doesn't have return type
//              - You can overload the Constructor

//  Types of Constructor
//              - Default: In can you didn't implementer any constructor, compiler will make it for you during compilation.
//              - No-arg: no arguments will be passed for it, it can have body.
//              - Parametrized: there are arguments, same coming examples.

public class ConstructorIntro {

    ConstructorIntro(){
        System.out.println("Hello from the Constructor");
    }
}


